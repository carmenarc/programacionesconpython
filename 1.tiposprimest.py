# Enteros (int)
mi_entero = 42
print("Entero:", mi_entero)

# Flotantes (float)
mi_real = 3.14
print("Flotante:", mi_real)

# Cadenas (str)
mi_cadena = "Hola, mundo"
print("Cadena:", mi_cadena)

# Listas (arrays)
mi_lista = [10, 20, 30]
print("Lista:", mi_lista)

# Diccionarios (estructuras)
mi_diccionario = {"nombre": "Juan", "edad": 25}
print("Diccionario:", mi_diccionario)

# Enumerados (enum)
from enum import Enum
class Color(Enum):
    ROJO = 1
    VERDE = 2
    AZUL = 3
print("Enumerado:", Color.ROJO)
