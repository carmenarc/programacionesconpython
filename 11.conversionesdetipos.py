# Conversión de un valor numérico decimal a su parte entera
decimal = 3.75
parte_entera = int(decimal)
print("Valor decimal:", decimal)
print("Parte entera:", parte_entera)

# Conversión de una cadena a un número entero
cadena_entero = "123"
numero_entero = int(cadena_entero)
print("\nCadena:", cadena_entero)
print("Número entero:", numero_entero)

# Conversión de una cadena a un número decimal
cadena_decimal = "3.14"
numero_decimal = float(cadena_decimal)
print("\nCadena:", cadena_decimal)
print("Número decimal:", numero_decimal)

# Conversión de un número entero a una cadena
entero = 42
cadena_entero = str(entero)
print("\nNúmero entero:", entero)
print("Cadena:", cadena_entero)

