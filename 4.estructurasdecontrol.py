#Estructura if
x = 17
if x < 20:
    print('x es menor que 20')
    
#Estructura if-else
valores = [1, 3, 4, 8]
if 5 in valores:
    print('Está en valores')
else:
    print('No está en valores')
    
# Estructura if-elif-else
numero = 10
if numero > 0:
    print('Número positivo')
elif numero < 0:
    print('Número negativo')
else:
    print('Número igual a cero')

#Estructura bucle for
numeros = [1, 2, 3, 4, 5]
for num in numeros:
    print(num)

#Estructura bucle while
contador = 5
while contador > 0:
    print(contador)
    contador -= 1

